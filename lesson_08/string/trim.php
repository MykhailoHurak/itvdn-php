<?php

// Видалення пробілів або інших символів з рядка

// trim - видалення з початку та кінця
// ltrim - видалення з початку
// rtrim - видалення з кінця

$str = '+++Це кілька слів+';

$result = rtrim($str, '+');

// виведення відформатованого рядка
echo "<pre>";
echo $str;
echo "<br>";
echo $result;
echo "</pre>";